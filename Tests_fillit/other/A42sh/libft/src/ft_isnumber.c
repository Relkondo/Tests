#include "libft.h"

int		ft_isnumber(char *str)
{
	if (!str)
		return (0);
	while (*str == ' ' || *str == '\t' || *str == '\n')
		str++;
	if (*str == '+' || *str == '-')
		str++;
	return (ft_isnumeric(str));
}
